package sk.itlearning.java1.e.operatory;

public class O1_Porovnavacie {

	// == != > >= < <=
	public static void main(String[] args) {

		// vysledkom porovnania je logicka (booleanovska) hodnota true alebo false
		int a = 5;
		int b = 5;

		System.out.println(a == b);
		System.out.println(a != b);
		System.out.println(a > b);
		System.out.println(a >= b);

	}

}
